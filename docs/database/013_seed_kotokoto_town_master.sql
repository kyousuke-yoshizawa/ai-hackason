--
-- 背景：
--   要件定義書v2の架空エリア「ことこと町」8店舗（のんびり亭・ことりの休憩処・つきみ座・
--   まんまるパンや・ふわふわ雑貨店・ひなた文庫・きらきらアイス堂・
--   おひるねファミリー食堂）は、これまでのどのシードSQL（008/009等）にも存在しない。
--   既存シード（ことこと食堂・ことこと珈琲・ことこと雑貨店・麺屋一福・
--   ふわふわパン工房・はなよし花店・本の森書店・酒場つばめ・ひなた雑貨店 の計9件）は
--   別物のテストデータであり、本SQLでは削除せず併存させる（本SQLはINSERTのみ）。
--
-- ⚠️ 店舗名について：要件定義書の元の店舗名から2件が商標調査の結果、改名されている
--    （Issue #152 対応済み）。本SQLは新名称を使用する：
--      × ことりカフェ → ○ ことりの休憩処
--      × ちいさな本の森 → ○ ひなた文庫
--
-- 座標変換について：要件定義書の座標は原点(0,0)中心・負値を含むが、実装
-- （MapPicker/StoreForm、stores.x/y列）は 0〜400 の正値のみを扱うため、
-- オフセット (+200, +200) を適用した変換後座標を投入する
-- （変換表は Issue #101 本文 / 要件定義書v2 2.2.1節を参照。以下は変換後の値）。
--
-- オファーについて：要件の「オファー」列（例: 平日14–16時15%OFF）は、店舗側の
-- 時間帯別オファー機能（要件定義書v2 S004）に対応するDBスキーマが未実装のため、
-- 本SQLでは投入しない（各店舗のコメントに要件どおりの内容を記載するに留める。
-- backend/domains/plan/promptBuilder.ts の既存コメントと同様の扱い）。
--
-- 混雑パターン（crowd_patterns）について：
--   crowd_patterns テーブル（007_create_crowd_patterns_table.sql）には曜日次元が無く
--   store_id × hour_of_day の組でしか混雑度を持てない（曜日次元の追加はIssue #99）。
--   要件の混雑ルール文字列は「平日/土日で異なる」ケースがあるため、以下の優先順位で
--   曜日を区別しない単一の時間帯別パターンに変換している：
--     1. いずれかの曜日区分で「混」と明記された時間帯 → high
--     2. 「混」の指定が無く、いずれかの曜日区分で「空」と明記された時間帯 → low
--     3. それ以外（無指定 or 「普通」） → medium
--   時間帯の範囲表記「A–B時」は半開区間 [A, B) として時単位に変換する
--   （例: 「12–14時」→ hour_of_day 12, 13）。
--   Issue #99 で曜日次元が追加された際は、本コメントの平日/土日の内訳を参照して
--   分割してください。
--
-- 実行方法：Supabase ダッシュボード > SQL Editor に貼り付けて実行してください（未実行）。
-- べき等性：stores は固定UUID + ON CONFLICT (id) DO NOTHING で再実行安全。
--          crowd_patterns は (store_id, hour_of_day) の一意制約があるため
--          ON CONFLICT DO NOTHING で再実行安全。

