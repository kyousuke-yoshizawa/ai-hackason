--
-- 対応：
--   1. 本SQLで、動かない旧store_media行を全て削除する
--   2. その後、docs/scratchpad等に用意した「どうぶつの森風」いらすとやイラスト画像を
--      実際にアプリの管理画面「メディア管理」からドラッグ＆ドロップでアップロードする
--      （Storage RLSの都合上、SQL/anonキー経由でのアップロードはできないため、
--      認証済みの管理画面から行う必要がある）
--
-- 実行方法：Supabase ダッシュボード > SQL Editor に貼り付けて実行してください。

